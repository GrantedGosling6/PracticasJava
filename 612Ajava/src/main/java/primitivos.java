public class primitivos {
    public static void main(String[] args) {
        byte numeritito = 127;
        short numerito = 512;
        int numero= 45676757;
        long numerote = 3534634634678578L;
        float decimalcorto = 124235.335F;
        double decimallargo = 24225.3474734;
        char letra = 'b';
        boolean logico= true;
        
        
        System.out.println("El tipo byte: "+numeritito);
        System.out.println("El tipo short: "+numerito);
        System.out.println("El tipo entero: "+numero);
        System.out.println("El tipo long: "+numerote);
        System.out.println("El tipo float: "+decimalcorto);
        System.out.println("El tipo double: "+decimallargo);
        System.out.println("El tipo char: "+letra);
        System.out.println("El tipo booleano es: "+logico);
    }
    
}
